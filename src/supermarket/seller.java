    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
        // TODO add your handling code here:
        if (selid.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null,"select the seller to be Deleted ");
        }
        else{
          DefaultTableModel df = (DefaultTableModel)sellertable.getModel();
        int selectedIndex = sellertable.getSelectedRow();
            try{
                
            Connection con= db.dbconnect();
            int id = Integer.parseInt(df.getValueAt(selectedIndex, 0).toString());
           PreparedStatement pst = conn.prepareStatement("DELETE FROM seller  WHERE selid=?");
            pst.setInt(1,id);
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(null, "Record Deleted");
            show_table();
            selid.setText("");
            selname.setText("");
            selpass.setText("");
            selid.requestFocus();
                
            }catch(Exception e)
            {
                e.printStackTrace();
                System.out.println(e);
            }
        }
    }//GEN-LAST:event_jButton3MouseClicked

